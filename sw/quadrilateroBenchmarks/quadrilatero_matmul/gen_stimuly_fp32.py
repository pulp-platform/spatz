def write_arr(f, name, arr, ctype, size):
    f.write(ctype + " " + name + "[] = {\n\t")
    i = 1
    for v in arr:
        if i % (size*size) == 0:
            f.write('%f};\n\n' % (v))
        elif i % size == 0:
            f.write('%f,\n\t' % (v))
        else:
            f.write('%f,' % (v))
        i+=1
    return


################################################################################
f = open('matrixMul32f.h', 'w')
f.write('#ifndef _MATMUL64FP32_\n')
f.write('#define _MATMUL64FP32_\n')
f.write('// This file is automatically generated\n')


SIZE = 64
RANGE = 1.99999

m_a   = []
m_b   = []
m_exp = []

for i in range(0,SIZE):
    for j in range(0,SIZE):
        a = random.uniform(-RANGE, RANGE)
        b = random.uniform(-RANGE, RANGE)

        m_a.append(a)
        m_b.append(b)

for i in range(0,SIZE):
    for j in range(0,SIZE):
        r = 0

        for k in range (0,SIZE):
            r = r + m_a[i * SIZE + k] * m_b[j * SIZE + k]

        m_exp.append(r)


write_arr(f, 'matrix_A'  ,   m_a, 'float __attribute__((section(".xheep_data_interleaved")))', SIZE)
write_arr(f, 'matrix_BT' ,   m_b, 'float __attribute__((section(".xheep_data_interleaved")))', SIZE)
write_arr(f, 'matrix_EXP', m_exp, 'float __attribute__((section(".xheep_data_interleaved")))', SIZE)

f.write('#define SIZE %d\n' % SIZE)


f.write('#endif')
